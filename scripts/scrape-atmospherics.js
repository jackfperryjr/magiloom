// Scrapes atmospheric-item messages from Elanthipedia and regenerates
//   src/renderer/src/lib/atmospherics.generated.ts
//
// Atmospheric items (https://elanthipedia.play.net/Category:Atmospheric_items)
// emit fixed first-person ("your <item>…") and third-person ("<Player>'s <item>…")
// flavor to the room. DR sends these untagged on the main stream, so the app
// matches them by exact text to route them to the Atmo panel. This list changes
// slowly; re-run this script periodically (e.g. quarterly) and commit the result.
//
// Usage: node scripts/scrape-atmospherics.js

const fs = require('fs')
const path = require('path')

const API = 'https://elanthipedia.play.net/api.php'
const CATEGORY = 'Category:Atmospheric_items'
const OUT = path.join(__dirname, '..', 'src', 'renderer', 'src', 'lib', 'atmospherics.generated.ts')

async function api(params) {
  const url = API + '?' + new URLSearchParams({ ...params, format: 'json' })
  for (let attempt = 0; attempt < 4; attempt++) {
    try {
      const res = await fetch(url, { headers: { 'User-Agent': 'Magiloom-atmo-scraper/1.0' } })
      if (res.ok) return res.json()
    } catch { /* retry */ }
    await new Promise(r => setTimeout(r, 500 * (attempt + 1)))
  }
  throw new Error('API failed: ' + url)
}

async function allCategoryTitles() {
  const titles = []
  let cont
  do {
    const data = await api({
      action: 'query', list: 'categorymembers',
      cmtitle: CATEGORY, cmlimit: '500', cmtype: 'page',
      ...(cont ? { cmcontinue: cont } : {}),
    })
    for (const m of data.query.categorymembers) if (m.title.startsWith('Item:')) titles.push(m.title)
    cont = data.continue?.cmcontinue
  } while (cont)
  return titles
}

const cleanLine = s => s
  .replace(/<br\s*\/?>/gi, ' ')
  .replace(/^\s*(?:First|Third)[\s-]*Person:?\s*/i, '')   // drop perspective label
  .replace(/'''?/g, '')                                   // wiki bold
  .replace(/\[\[[^\]|]*\|([^\]]*)\]\]/g, '$1')            // [[link|text]] → text
  .replace(/\[\[([^\]]*)\]\]/g, '$1')                     // [[link]] → link
  .replace(/\s+/g, ' ')
  .trim()

// A real atmo message is a verbatim prose sentence, not wiki markup or a wiki
// note describing the messaging.
const isMessage = s =>
  s.length >= 20 && s.length <= 400 &&
  /^[A-Z"<[]/.test(s) &&                    // starts like a sentence or a placeholder
  /[.!?"]$/.test(s) &&                      // ends like a sentence
  /\s/.test(s) &&                           // multiple words
  !/\{\{|\}\}|\|\|/.test(s) &&              // not a template/table row
  !/atmospheric messag|messaging (?:when|is|will|occurs|for)|will atmo|atmo at a time|random atmospher|see below|refer to|no known|unknown|when worn|when in (?:weapon|combat) forms|this item/i.test(s) &&
  !/^(You focus|You recognize|Roundtime|Experience with|Note:)/i.test(s)

function extract(wikitext) {
  const regions = []
  // Format A: "==Atmo…==" section up to the next heading, template, or end.
  const secRe = /==+\s*(?:Atmo|Messaging|Ambient)[^=\n]*==+([\s\S]*?)(?=\n==[^=]|\n\{\{|$)/gi
  let m
  while ((m = secRe.exec(wikitext))) regions.push(m[1])
  // Format B: inline "Atmo messaging:" block (First/Third Person lines).
  const inl = wikitext.match(/Atmo messaging:?([\s\S]*?)(?=\n==|\n\n\n|$)/i)
  if (inl) regions.push(inl[1])

  const out = new Set()
  for (const region of regions) {
    for (const raw of region.split(/<br\s*\/?>|\r?\n/i)) {
      const line = cleanLine(raw)
      if (isMessage(line)) out.add(line)
    }
  }
  return out
}

async function main() {
  process.stdout.write('Fetching category members… ')
  const titles = await allCategoryTitles()
  console.log(titles.length + ' items')

  const messages = new Set()
  let miss = 0

  for (let i = 0; i < titles.length; i += 50) {
    const batch = titles.slice(i, i + 50)
    const data = await api({
      action: 'query', prop: 'revisions', rvprop: 'content', rvslots: 'main',
      titles: batch.join('|'),
    })
    const pages = data.query?.pages ?? {}
    for (const p of Object.values(pages)) {
      const wt = p.revisions?.[0]?.slots?.main?.['*'] ?? p.revisions?.[0]?.['*'] ?? ''
      const found = extract(wt)
      if (found.size === 0) miss++
      for (const msg of found) messages.add(msg)
    }
    process.stdout.write(`\rParsed ${Math.min(i + 50, titles.length)}/${titles.length}…`)
  }
  console.log(`\nExtracted ${messages.size} atmo messages (${miss}/${titles.length} items had none parseable)`)

  const arr = [...messages].sort().map(s => '  ' + JSON.stringify(s)).join(',\n')
  const banner = `// AUTO-GENERATED by scripts/scrape-atmospherics.js — do not edit by hand.\n` +
    `// Source: ${CATEGORY} (${titles.length} items, ${new Date().toISOString().slice(0, 10)}).\n` +
    `// Verbatim atmospheric-item messages; placeholders like [Location] / <Player>\n` +
    `// are turned into wildcards by lib/atmospherics.ts. Re-run the script to refresh.\n\n`
  fs.writeFileSync(OUT, banner + `export const ATMO_MESSAGES: string[] = [\n${arr}\n]\n`, 'utf8')
  console.log('Wrote ' + path.relative(path.join(__dirname, '..'), OUT))
}

main().catch(e => { console.error(e); process.exit(1) })
